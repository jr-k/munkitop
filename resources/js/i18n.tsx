export type TranslationKey = keyof typeof translations.en;

const I18nContext = createContext<I18nContextValue | null>(null);

function detectedLocale(): Locale {
    if (typeof window === 'undefined') {
        return 'en';
    }

    const stored = window.localStorage.getItem(STORAGE_KEY);

    if (stored === 'en' || stored === 'fr') {
        return stored;
    }

    return navigator.language.toLowerCase().startsWith('fr') ? 'fr' : 'en';
}

export function I18nProvider({ children }: PropsWithChildren) {
    const [locale, setLocaleState] = useState<Locale>(detectedLocale);

    const value = useMemo<I18nContextValue>(() => {
        function setLocale(nextLocale: Locale) {
            window.localStorage.setItem(STORAGE_KEY, nextLocale);
            setLocaleState(nextLocale);
        }

        function t(key: TranslationKey, params: Record<string, string | number> = {}) {
            return Object.entries(params).reduce(
                (message, [param, replacement]) => message.replaceAll(`{${param}}`, String(replacement)),
                translations[locale][key] ?? translations.en[key],
            );
        }

        return { locale, setLocale, t };
    }, [locale]);

    return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
    const context = useContext(I18nContext);

    if (!context) {
        throw new Error('useI18n must be used inside I18nProvider');
    }

    return context;
}
